                        <h5 class="mt-4">üë®‚Äçüç≥ Cooking Process</h5>
                        <ul>
                            <li>Prepare the pork cutlets by breading and frying them until golden and crispy (or use pre-made tonkatsu).</li>
                            <li>In a pan, heat the soy sauce, mirin, dashi, and sugar. Add the sliced onions and simmer for a few minutes until softened.</li>
                            <li>In a bowl, beat the eggs. Pour the eggs into the pan with the onion mixture, covering the onions and letting it cook gently.</li>
                            <li>Slice the tonkatsu into strips and place it on top of a bowl of hot rice. Pour the egg mixture and onions over the pork cutlet.</li>
                            <li>Serve immediately with pickled vegetables or miso soup on the side.</li>
                        </ul>
