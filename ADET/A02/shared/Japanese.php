                            <li>1/2 onion (sliced)</li>
                            <li>2 eggs</li>
                            <li>1/4 cup soy sauce</li>
                            <li>1/4 cup mirin</li>
                            <li>1/4 cup dashi or water</li>
                            <li>1 tbsp sugar</li>
